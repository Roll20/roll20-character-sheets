/**
 * @typedef Roll20Event
 * @type {object}
 * @property {string} sourceAttribute - The attribute name of the change; undefined for button events.
 * @property {string} triggerName - The name of the trigger including the trigger type. E.g. `change:attribute_name` or `clicked:button-name`
 * @property {string} sourceType - Who made the change; player (or API) or sheetworker
 * @property {string|number} previousValue - The value of the attribute before the change was made
 * @property {string|number} newValue - What the attribute value was changed to
 * @property {object} removedInfo - The contents of the removed repeating row
 */

